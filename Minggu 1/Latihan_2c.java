public class Latihan_2c {
    public static void main (String [] args)
    {
        double D,T,PI,H;

        D=5*0.5;       
        T=10;          
        PI=3.14;       
        H=PI * D * D * T;
        
        System.out.println ("Berikut adalah hasil dari volume tabung dengan diameter = 5 dan tinggi = 10");
        System.out.println ("Volume Tabung = "+H);
    }
}
